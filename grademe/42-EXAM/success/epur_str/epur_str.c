#include <unistd.h>

int main(int ac, char **av)
{
    if (ac != 2)
        return (write(1, "\n", 1), 0);

    int i = 0;
    int w = 0;

    while (av[1][i] == ' ' || av[1][i] == '\t')
        i++;

    while (av[1][i])
    {
        if (w)
            write(1, " ", 1);

        while (av[1][i] && av[1][i] != ' ' && av[1][i] != '\t')
        {
            write(1, &av[1][i], 1);
            i++;
        }

        w = 1;
        while (av[1][i] == ' ' || av[1][i] == '\t')
            i++;
    }
    write(1, "\n", 1);
}

