#include <unistd.h>

int main(int ac, char **av)
{
	if (ac < 2)
		return write(1, "\n", 1);

	char *s1 = av[1];
	int i = 0;
	while(s1[i])
	{
		write(1, &s1[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
