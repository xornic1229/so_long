#include <unistd.h>

int main(int ac, char **av)
{
	if (ac != 2)
		return write(1, "\n", 1);

	int i = 0;

	while (av[1][i] == ' ' || av[1][i] == '\t')
		i++;
	while (av[1][i] >= 33 && av[1][i] <= 127)
	{
		write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
}
