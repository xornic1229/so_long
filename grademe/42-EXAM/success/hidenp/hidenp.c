#include <unistd.h>

int main(int ac, char **av)
{
    if (ac != 3)
    {
        write(1, "\n", 1);
        return 0;
    }

    int i = 0;
    int j = 0;

    while (av[1][i])
    {
        while (av[2][j])
        {
            if (av[2][j] == av[1][i])
            {
                i++;
                break;
            }
            j++;
        }
        if (!av[2][j])
        {
            write(1, "0\n", 2);
            return 0;
        }
        j++;
    }
    write(1, "1\n", 2);
    return 0;
}
